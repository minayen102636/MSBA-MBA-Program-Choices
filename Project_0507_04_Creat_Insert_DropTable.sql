CREATE TABLE [TurboRank.University](
      uId CHAR(3) NOT NULL,
      uniName VARCHAR (60),
      CONSTRAINT pk_University_uId PRIMARY KEY (uId)
      )
CREATE TABLE [TurboRank.UniversityLoc] (
      uId CHAR(3) NOT NULL,
      uniLoc VARCHAR (20),
      CONSTRAINT pk_UniversityLoc_uId_uniLoc PRIMARY KEY (uId,uniLoc),
      CONSTRAINT fk_UniversityLoc_uId FOREIGN KEY (uId)
            REFERENCES [TurboRank.University] (uId)
            ON DELETE CASCADE ON UPDATE CASCADE
      )
CREATE TABLE [TurboRank.Program] (
      pId CHAR(3) NOT NULL,
      uId CHAR(3) NOT NULL,
      prgDuration DECIMAL (3,1),
      prgName VARCHAR(60) NOT NULL,
      CONSTRAINT pk_Program_pId PRIMARY KEY (pId),
      CONSTRAINT fk_Program_uId FOREIGN KEY (uId)
            REFERENCES [TurboRank.University] (uId)
            ON DELETE CASCADE ON UPDATE CASCADE
      )
CREATE TABLE[TurboRank.ProgramInformation](
      pId CHAR(3) NOT NULL,
      prgRankYear CHAR (4) NOT NULL,
      prgAcceptance DECIMAL (4,3),
      prgMeanGMAT DECIMAL (5,2),
      prgMeanGRE DECIMAL (5,2),
      prgMeanTOEFL DECIMAL (5,2),
      prgMeanUndergradGPA DECIMAL(3,2),
      prgIntStudentRate DECIMAL (4,3),

      prgFemaleStudentRate DECIMAL (4,3),
      prgEmploymentRate DECIMAL (4,3),
      prgMeanStartSalary DECIMAL (9,2),
      prgTuition DECIMAL (9,2),
      prgFacultyStudentRate DECIMAL (4,3),
      CONSTRAINT pk_ProgramInformation_pId_rankYear PRIMARY KEY (pId, prgRankYear),
      CONSTRAINT fk_ProgramInformation_pId FOREIGN KEY (pId)
            REFERENCES [TurboRank.Program] (pId)
            ON DELETE CASCADE ON UPDATE CASCADE
      )
CREATE TABLE [TurboRank.Course] (
      cId CHAR(4) NOT NULL,
      crsName VARCHAR (100),
      crsCredit DECIMAL (3,1),
      CONSTRAINT pk_Course_cId PRIMARY KEY (cId)
      )
CREATE TABLE [TurboRank.Have] (
      pId CHAR(3) NOT NULL,
      cId CHAR(4) NOT NULL,
      CONSTRAINT pk_Have_pId_cId PRIMARY KEY (pId, cId),
      CONSTRAINT fk_Have_cId FOREIGN KEY (cId)
            REFERENCES [TurboRank.Course] (cId)
            ON DELETE CASCADE ON UPDATE CASCADE,
      CONSTRAINT fk_Have_pId FOREIGN KEY (pId)
            REFERENCES [TurboRank.Program] (pId)
            ON DELETE CASCADE ON UPDATE CASCADE
      )
CREATE TABLE [TurboRank.Associate] (
      uId CHAR(3) NOT NULL,
      rId CHAR(3) NOT NULL,
      uniRankYear CHAR(4) NOT NULL,
      uniRank VARCHAR (7),
      CONSTRAINT pk_Associate_uId PRIMARY KEY (uId, rId, uniRankYear),
      CONSTRAINT fk_Associate_uId FOREIGN KEY (uId)
            REFERENCES [TurboRank.University] (uId)
            ON DELETE NO ACTION ON UPDATE CASCADE,
      CONSTRAINT fk_Associate_rId FOREIGN KEY (rId)
            REFERENCES [TurboRank.RankInstitution] (rId)
            ON DELETE NO ACTION ON UPDATE CASCADE
      )
